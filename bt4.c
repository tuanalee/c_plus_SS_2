#include <stdio.h>

int main() {
    
    float canh = 5.0;  
    
  
    float chuVi = 4 * canh;      
    float dienTich = canh * canh; 

    
    printf("Canh hình vuông: %.2f\n", canh);
    printf("Chu vi cua hình vuông là: %.2f\n", chuVi);
    printf("Dien tích cua hình vuông là: %.2f\n", dienTich);

    return 0;
}
